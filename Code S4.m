xlim(ax4,...
    [datetime(2024,11,5) datetime(2024,11,21)]);

datetick(ax4,'x','mm/dd','keepticks');

box(ax4,'on');

ax4.LineWidth = AX_LINE_W;
ax4.FontName  = FONT_NAME;
ax4.FontSize  = FS_AXIS;

xlabel(ax4,'Date','FontSize',FS_LABEL);

legend(ax4,[h1 h2],...
    {'SST anomaly','SSS anomaly'},...
    'Location','southwest');

